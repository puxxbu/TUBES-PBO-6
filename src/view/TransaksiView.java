    private void saveBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtn1ActionPerformed
        // TODO add your handling code here:
        try {
            inputKosongException();
            String bonusPembelian = "";
            
            int selectedIndex = computerDropDown.getSelectedIndex();
            Computer selectedComputer = listComputer.get(selectedIndex);
            
            selectedIndex = pembeliDropDown.getSelectedIndex();
            Pembeli selectedPembeli = listPembeli.get(selectedIndex);
            
            
            if (mouseCheckBox.isSelected() && !tasCheckBox.isSelected() && !mousepadCheckBox.isSelected()  ) {
                bonusPembelian = "Mouse,-,-";
            }else if (mouseCheckBox.isSelected() && tasCheckBox.isSelected() && !mousepadCheckBox.isSelected()) {
                bonusPembelian = "Mouse,Tas,-";
            }else if (mouseCheckBox.isSelected() && tasCheckBox.isSelected() && mousepadCheckBox.isSelected()) {
                bonusPembelian = "Mouse,Tas,Mousepad";
            }else if (!mouseCheckBox.isSelected() && tasCheckBox.isSelected() && !mousepadCheckBox.isSelected()) {
                bonusPembelian = "-,Tas,-";
            }else if (mouseCheckBox.isSelected() && tasCheckBox.isSelected() &&! mousepadCheckBox.isSelected()) {
                bonusPembelian = "Mouse,Tas,-";
            }else if (mouseCheckBox.isSelected() && !tasCheckBox.isSelected() && mousepadCheckBox.isSelected()) {
                bonusPembelian = "Mouse,-,Mousepad";
            }else if (!mouseCheckBox.isSelected() && tasCheckBox.isSelected() && mousepadCheckBox.isSelected()) {
                bonusPembelian = "-,Tas,Mousepad";
            }else{
                bonusPembelian = "-,-,-";
            }
            
            if (action.equals("Tambah")) {
                Transaksi tr = new Transaksi(tglTransaksiInput.getText(), 
                        totalHargaInput.getText(), 
                        bonusPembelian, 
                        selectedComputer, 
                        selectedPembeli);
                
                
                transaksiControl.insertTransaksi(tr);
            }else{
                Transaksi tr = new Transaksi(selectedId,
                        tglTransaksiInput.getText(), 
                        totalHargaInput.getText(), 
                        bonusPembelian, 
                        selectedComputer, 
                        selectedPembeli);
                
                
                transaksiControl.updateTransaksi(tr);
            }
            clearText();
            showTransaksi();
            setComponent(false);
            
            
            
            setEditDeleteBtn(false);
            
        } catch (InputKosongException e) {
            JOptionPane.showMessageDialog(this, e.message());
        }
    }//GEN-LAST:event_saveBtn1ActionPerformed


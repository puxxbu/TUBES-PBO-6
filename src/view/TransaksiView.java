    private void tableTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTransaksiMouseClicked
        // TODO add your handling code here:
        int indexComputer = -1;
        int indexPembeli = -1;
        setEditDeleteBtn(true);
        setComponent(false);
        
        
        
        int clickedRow = tableTransaksi.getSelectedRow();
        
        TableModel tableModel = tableTransaksi.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 8).toString());
        tglTransaksiInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        totalHargaInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
        
        
        String bonusPembelian = tableModel.getValueAt(clickedRow, 5).toString();
        switch (bonusPembelian) {
            case "Mouse,-,-":
                mouseCheckBox.setSelected(true);
                tasCheckBox.setSelected(false);
                mousepadCheckBox.setSelected(false);
                break;
             case "Mouse,Tas,-":
                mouseCheckBox.setSelected(true);
                tasCheckBox.setSelected(true);
                mousepadCheckBox.setSelected(false);
                break;
             case "Mouse,Tas,Mousepad":
                mouseCheckBox.setSelected(true);
                tasCheckBox.setSelected(true);
                mousepadCheckBox.setSelected(true);
                break;
             case "-,Tas,Mousepad":
                mouseCheckBox.setSelected(false);
                tasCheckBox.setSelected(true);
                mousepadCheckBox.setSelected(true);
                break;
             case "-,Tas,-":
                mouseCheckBox.setSelected(false);
                tasCheckBox.setSelected(true);
                mousepadCheckBox.setSelected(false);
                break;
             case "Mouse,-,Mousepad":
                mouseCheckBox.setSelected(true);
                tasCheckBox.setSelected(false);
                mousepadCheckBox.setSelected(true);
                break;
             case "-,-,Mouse":
                mouseCheckBox.setSelected(false);
                tasCheckBox.setSelected(false);
                mousepadCheckBox.setSelected(true);
                break;
             case "-,-,-":
                mouseCheckBox.setSelected(false);
                tasCheckBox.setSelected(false);
                mousepadCheckBox.setSelected(false);
                break;
            case "-,-,Mousepad":
                mouseCheckBox.setSelected(false);
                tasCheckBox.setSelected(false);
                mousepadCheckBox.setSelected(true);
                break;
           
          
               
            default:
                throw new AssertionError();
        }
        
        String idComputer = tableModel.getValueAt(clickedRow, 10).toString();
        
       
        
        for (Computer computer : listComputer) {
            
            if (computer.getId().equals(idComputer) ) {
                indexComputer = listComputer.indexOf(computer);
            }
        }
        
        computerDropDown.setSelectedIndex(indexComputer);
       
        int idPembeli = Integer.parseInt(tableModel.getValueAt(clickedRow, 9).toString());
        
        for (Pembeli pembeli : listPembeli) {
            if (pembeli.getId() == idPembeli ) {
                indexPembeli = listPembeli.indexOf(pembeli);
            }
        }
        
        pembeliDropDown.setSelectedIndex(indexPembeli);
    }                                            


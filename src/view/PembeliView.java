    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data ?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        
        switch (getAnswer) {
            case 0:
            try {
                pembeliControl.deletePembeli(selectedId);
                clearText();
                showPembeli();
                setComponent(false);
                setEditDeleteBtn(false);
                
                
            } catch (Exception e) {
                System.out.println("Error : " + e.getMessage());
            }
                break;
             case 1:
                break;
            default:
                throw new AssertionError();
        }
        
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        setEditDeleteBtn(false);
        setComponent(false);
        
        try {
            TablePembeli pembeli = pembeliControl.showDataPembeli(inputSearch.getText());
            
            if (pembeli.getRowCount() == 0) {
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan","Konfirmasi",JOptionPane.DEFAULT_OPTION);
                
                
            }else{
                tablePembeli.setModel(pembeli);
                
            }
            
            clearText();
        } catch (Exception e) {
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void tablePembeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablePembeliMouseClicked
        // TODO add your handling code here:

        setEditDeleteBtn(true);
        setComponent(false);

        int clickedRow = tablePembeli.getSelectedRow();
        
        TableModel tableModel = tablePembeli.getModel();
        
        selectedId = Integer.parseInt(tableModel.getValueAt(clickedRow, 0).toString());
        noTeleponInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        namaInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        alamatInput.setText(tableModel.getValueAt(clickedRow, 2).toString());
        
        
    }                                            


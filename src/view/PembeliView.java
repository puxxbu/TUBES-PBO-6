    private void saveBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtn1ActionPerformed
        // TODO add your handling code here:
        try {
            
           
            
            
            
            
            
            
            if (action.equals("Tambah")) {
                Pembeli p = new Pembeli(namaInput.getText(),
                        alamatInput.getText(),
                        noTeleponInput.getText());
                
                
                pembeliControl.insertPembeli(p);
            }else{
                Pembeli p = new Pembeli(selectedId,
                        namaInput.getText(),
                        alamatInput.getText(),
                        noTeleponInput.getText());
                
                
                pembeliControl.updatePembeli(p);
            }
            clearText();
            showPembeli();
            setComponent(false);
            
            
            
            setEditDeleteBtn(false);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
    }//GEN-LAST:event_saveBtn1ActionPerformed


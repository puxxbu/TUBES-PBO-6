    public void inputKosongException() throws InputKosongException{
        if (idInput.getText().isEmpty() || namaInput.getText().isEmpty()
                || processorInput.getText().isEmpty() 
                || kapasitasRAMInput.getText().isEmpty()) {
            throw  new InputKosongException();
        }
    }
    
    public void inputIDException() throws InputIDException{
        if (idInput.getText().length() < 5 || 
                idInput.getText().length() > 8) {
            throw new InputIDException();
        }
    }
    
    public void inputJenisException() throws InputJenisException{
        if (!jRadioLaptop.isSelected()  && !jRadioDesktop.isSelected() ) {
            throw  new InputJenisException();
            
        }
    }
    
    
    


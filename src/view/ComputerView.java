    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, 
                "Apakah yakin ingin menghapus data ? ", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
        
        if (getAnswer == JOptionPane.YES_OPTION) {
            try {
                computerControl.deleteDataComputer(idInput.getText());
                clearText();
                showComputer();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
            } catch (Exception e) {
                System.out.println("Error : "+e.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Data tidak jadi dihapus!");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        setEditDeleteBtn(true);
        setComponent(false);
        
        try {
            Computer computer = computerControl.searchComputer(inputSearch.getText());
            
            if (computer  == null) {
                clearText();
                setEditDeleteBtn(false);
                JOptionPane.showConfirmDialog(null, "Data tidak ditemukan",
                        "Konfirmasi", JOptionPane.DEFAULT_OPTION);
            }else{
                idInput.setText(computer.getId());
                namaInput.setText(computer.getNama());
                processorInput.setText(computer.getJenis());
                kapasitasRAMInput.setText(Integer.toString(computer.getKapasitasRAM()));
                if (computer.getJenis().equalsIgnoreCase("Desktop")) {
                    jRadioDesktop.setSelected(true);
                    dayaListrikInput.setText(Integer.toString(computer.getDayaListrik()));
                }else{
                    jRadioLaptop.setSelected(true);
                    bateraiInput.setText(Integer.toString(computer.getKapasitasBaterai()));
                }
            }
        } catch (Exception e) {
            System.out.println("Error : "+e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin Menambah/Ubah data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        switch (getAnswer) {
            case 0: //jika jawaban user adalah Yes
                try {
                inputKosongException();
                inputIDException();
                inputJenisException();

                String jenis = "";
                Computer c = null;


                if (jRadioLaptop.isSelected() == true) {
                    jenis = "Laptop";

                    c = new Computer(idInput.getText(), namaInput.getText(),
                       processorInput.getText(), jenis,
                       Integer.parseInt(kapasitasRAMInput.getText()), 
                       0,Integer.parseInt(bateraiInput.getText()));
                }else if (jRadioDesktop.isSelected() == true) {
                    jenis = "Desktop";

                    c = new Computer(idInput.getText(), namaInput.getText(),
                        processorInput.getText(),jenis ,
                       Integer.parseInt(kapasitasRAMInput.getText()), 
                       Integer.parseInt(dayaListrikInput.getText()),0);
                }


                if (action.equals("Tambah")) {
                    computerControl.insertDataComputer(c);
                    JOptionPane.showMessageDialog(null, "Data berhasil ditambah!");
                }else{
                    computerControl.updateDataComputer(c, idInput.getText());
                     JOptionPane.showMessageDialog(null, "Data berhasil diubah!");

                }
                clearText();
                showComputer();
                setComponent(false);
                setEditDeleteBtn(false);



            } catch (InputKosongException e) {
                JOptionPane.showMessageDialog(this, e.message());
            } catch ( NumberFormatException e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            } catch ( InputIDException e){
                JOptionPane.showMessageDialog(this, e.message());
            } catch ( InputJenisException e){
                JOptionPane.showMessageDialog(this, e.message());
            } 
            break;
            case 1: 
                JOptionPane.showMessageDialog(null, "Data tidak jadi ditambah/ubah!");
            break;
        }
        
        
    }//GEN-LAST:event_saveBtnActionPerformed

